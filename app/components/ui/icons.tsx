/* ─── Icon Registry (Strategy Pattern) ─── */

const ICON_MAP: Record<string, (props: IconProps) => ReactNode> = {
  github: GithubIcon,
  youtube: YoutubeIcon,
  external: ExternalIcon,
  email: EmailIcon,
  linkedin: LinkedInIcon,
  drive: DriveIcon,
};

/**
 * Resolve an icon component by name.
 * Uses Strategy pattern to avoid switch statements in consuming components.
 */
export function IconByName({
  name,
  size,
  className,
}: IconProps & { name: string }) {
  const IconComponent = ICON_MAP[name] ?? ExternalIcon;
  return <IconComponent size={size} className={className} />;
}
